#include "core/IWindow.h"
//---------------------------------------------------------------------------------------------------------------------
// Global
//---------------------------------------------------------------------------------------------------------------------
static IWindow* gpWindow = nullptr;

//---------------------------------------------------------------------------------------------------------------------
// Getters & Setters
//---------------------------------------------------------------------------------------------------------------------
IWindow* IWindow::Get(void)
{
	return gpWindow;
}

//---------------------------------------------------------------------------------------------------------------------
// Public Functions
//---------------------------------------------------------------------------------------------------------------------
IWindow::IWindow(void)
{
	if (gpWindow)
	{
		delete gpWindow;
	}
	gpWindow = this;
}

IWindow::~IWindow(void)
{
	if (gpWindow == this)
	{
		gpWindow = nullptr;
	}
}
