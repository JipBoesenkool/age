#ifndef MOUSEMOVEEVENT_H
#define MOUSEMOVEEVENT_H
//---------------------------------------------------------------------------------------------------------------------
// Includes
//---------------------------------------------------------------------------------------------------------------------
#include "BaseEvent.h"
//-------------------------------------------------------------------------------------------------------------------
// class MouseMoveEvent
//-------------------------------------------------------------------------------------------------------------------
class MouseMoveEvent : public BaseEvent
{
//-------------------------------------------------------------------------------------------------------------------
// Members
//-------------------------------------------------------------------------------------------------------------------
public:
	double m_xPos;
	double m_yPos;
//-------------------------------------------------------------------------------------------------------------------
// Functions
//-------------------------------------------------------------------------------------------------------------------
public:
	MouseMoveEvent(void)
	{
		m_eventName = "MouseMoveEvent";
	}

	explicit MouseMoveEvent(double xPos, double yPos)
	{
		m_eventName = "MouseMoveEvent";
		m_xPos = xPos;
		m_yPos = yPos;
	}

	virtual IEventPtr Copy(void) const
	{
		return IEventPtr( new MouseMoveEvent(m_xPos, m_yPos) );
	}
};
#endif //MOUSEMOVEEVENT_H
